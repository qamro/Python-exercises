# 2D list
list_2D = [['A', 'B', 'C', 'D'], ['Q', 'S', 'F', 'G'], ['H', 'J', 'K', 'L']]
# for accessing an element in our 2D list we use indexing
print(list_2D[0][0])
print(list_2D[0][1])
print(list_2D[2][3])
print(list_2D[2][0])
print(list_2D[1][2])
print(list_2D[0][3])

# 3D list
list_3D = [[['A', 'B', 'C', 'D'], ['Q', 'S', 'F', 'G'], ['H', 'J', 'K', 'L']],
            [['P', 'N', 'V', 'X'], ['W', 'M', '0', '9'], ['4', '3', '2', '1']],
            [['O', 'I', 'U', 'Y'], ['T', 'R', 'E', 'Z'], ['a', 'b', 'c', 'd']]]
# for accessing an element in our 3D list we use indexing
print(list_2D[0][0][0])
print(list_2D[0][1][1])
print(list_2D[2][2][2])
print(list_2D[0][2][3])
print(list_2D[0][1][3])
print(list_2D[2][0][3])